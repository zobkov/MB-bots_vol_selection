if docker ps -q -f name=vol_selection_postgres &> /dev/null; then
    docker exec vol_selection_postgres pg_dump -U $DB_USER $DB_NAME > $BACKUP_FILE
    echo "‚úÖ –†–µ–∑–µ—Ä–≤–Ω–∞—è –∫–æ–ø–∏—è —Å–æ–∑–¥–∞–Ω–∞: $BACKUP_FILE"
